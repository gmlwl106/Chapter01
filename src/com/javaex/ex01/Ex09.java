package com.javaex.ex01;

public class Ex09 {
	public static void main(String[] args) {
		
		//관계연산자
		int n1 = 5;
		int n2 = 3;
		
		//true/false 형식으로 나옴
		System.out.println(n1<n2);
		System.out.println(n1>n2);
		
		System.out.println(n1<=n2);
		System.out.println(n1>=n2);
		
		System.out.println(n1==n2);
		System.out.println(n1!=n2);
	}
}
